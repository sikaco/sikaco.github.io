function starAni (time) {
    var i = 0;
    var id = setInterval(function () {
        if (i++ < time) {
            var str = '';
            if (time <= 5) {
                str = '★★★★★'.substr(0,i) + '☆☆☆☆☆'.substr(i, 5);
            } else {
                for (var j = 0; j < time; j++) {
                    str += '★';
                }
            }
            $('#codeStar').text(str);
            console.log(str);
        } else {
            clearInterval(id);
        }
    }, 120);
}